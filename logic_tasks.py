"""
Задача 1. Проверка доступа к онлайн-курсу

Условие:
Пользователь проходит тестирование для доступа к премиум-курсу.
Доступ открывается если:

    Набрано 60 баллов или больше ИЛИ

    Куплен премиум-доступ

Напишите программу, которая определяет, получит ли пользователь
доступ к курсу.

Пример входных данных:

    Баллы за тест: 75

    Премиум-доступ: False
"""

### ваше решение здесь ###
test_points = int(input())
premium_access = input().lower() == "true"
if test_points >= 60 or premium_access:
    print("Доступ разрешен")
else:
    print("Доступ закрыт")


"""
Задача 2. Система скидок в магазине

Условие:
Интернет-магазин предоставляет скидку 10% при выполнении одного из условий:

    Сумма покупки ≥ 5000 рублей ИЛИ

    Пользователь имеет действующий промокод

Напишите программу, которая рассчитывает итоговую цену с учетом скидки.

Пример входных данных:

    Сумма покупки: 6000 рублей

    Промокод: False
"""

### ваше решение здесь ###
summa = int(input("Сумма покупки:"))
has_promocode = input("Наличие промокода (true/false): ").lower() == "true"
if summa >= 5000 or has_promocode:
    final_price = summa * 0.9
else:
    final_price = summa

print("Итоговая цена:", final_price)

"""
Задача 3. Проверка сложности пароля

Условие:
Система регистрации требует надежный пароль, 
который должен соответствовать требованиям:

    Длина не менее 8 символов И

    Содержит хотя бы одну цифру

Напишите программу, которая проверяет, 
удовлетворяет ли пароль требованиям безопасности.

Пример входных данных:

    Пароль: "secure123"
"""

### ваше решение здесь ###
import re

password = input("Введите пароль: ")
password_length = len(password)
numbers = re.search(r"\d+", password)
if password_length >= 8 and numbers:
    print("Пароль надежный")
else:
    print("Пароль ненадежный")

"""
Задача 4. Определение статуса студента

Условие:
Студент получает стипендию если выполняются оба условия:

    Средний балл ≥ 4.5 И

    Нет академических задолженностей

Напишите программу, которая определяет, положена ли студенту стипендия.

Пример входных данных:

    Средний балл: 4.7

    Задолженности: False
"""

### ваше решение здесь ###
