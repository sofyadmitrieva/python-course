"""

Задача 1. Вложенные условия (базовый синтаксис)

Условие:
Напишите программу, которая проверяет доступ к контенту
по возрасту и подписке.

Логика:

    Если возраст >= 18, проверяем подписку:
        - Если есть подписка: "Полный доступ"
        - Если нет подписки: "Только бесплатный контент"
    Если возраст < 18:
        - "Доступ ограничен"

Пример входных данных:
    Возраст: 20
    Подписка: True
"""

age = int(input())
sub = input().lower() == "true"

(
    print("Полный доступ")
    if age >= 18 and sub
    else (
        print("Только бесплатный контент")
        if age >= 18 and not sub
        else print("Доступ ограничен")
    )
)

